# მომხმარებლისგან რიცხვების დიაპაზონი
start = int(input("გთხოვთ შეიყვანოთ საწყისი რიცხვი: "))
end = int(input("გთხოვთ შეიყვანოთ დასრულების რიცხვი: "))

# for loop, რომ გადავავლოთ თვალს დიაპაზონზე
for num in range(start, end + 1):
    if num % 2 == 0 and num % 3 == 0:  # თუ რიცხვი 2-ისა და 3-ის ჯერადია
        print(f"{num} არის 3-ისა და 2-ის ჯერადი")
    else:
        print("რაც გინდათ ის")
